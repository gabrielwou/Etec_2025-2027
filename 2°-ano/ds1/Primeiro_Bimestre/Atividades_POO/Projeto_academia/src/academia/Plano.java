package academia;

public class Plano {
	private Boolean mensal;
	private Boolean trimestral;
	private Boolean anual;
	
	
	public Boolean getMensal() {
		return mensal;
	}
	public void setMensal(Boolean mensal) {
		this.mensal = mensal;
	}
	public Boolean getTrimestral() {
		return trimestral;
	}
	public void setTrimestral(Boolean trimestral) {
		this.trimestral = trimestral;
	}
	public Boolean getAnual() {
		return anual;
	}
	public void setAnual(Boolean anual) {
		this.anual = anual;
	}
}
