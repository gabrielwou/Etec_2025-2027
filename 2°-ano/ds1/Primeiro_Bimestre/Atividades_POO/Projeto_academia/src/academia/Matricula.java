package academia;

public class Matricula {
	private int codigoAluno;
	private int codigoInstrutor;
	private String planoEscolhido;
	
	public int getCodigoAluno() {
		return codigoAluno;
	}
	
	public void setCodigoAluno(int codigoAluno) {
		this.codigoAluno = codigoAluno;
	}
	
	public int getCodigoInstrutor() {
		return codigoInstrutor;
	}
	
	public void setCodigoInstrutor(int codigoInstrutor) {
		this.codigoInstrutor = codigoInstrutor;
	}
	
	public String getPlanoEscolhido() {
		return planoEscolhido;
	}
	
	public void setPlanoEscolhido(String planoEscolhido) {
		this.planoEscolhido = planoEscolhido;
	}
	
}
